#include <stdio.h>
//This program calculates the sum of x + y
int main()
{
    int x = 7;
    int y = 3;
    printf ("%i + %i = %i\n", x, y, x+y); //adds X + Y test
    printf ("%i + %i = %i\n", x, y, x-y); //Subtracts X-Y
    printf ("%i + %i = %i\n", x, y, x*y); //Multiplies X*Y
    printf ("%i + %i = %i\n", x, y, x/y); //Divides X/Y
    return 0;
}
